package com.example.firsttest.sintaxis

fun main() {
    val num1 = 5
    val num2 = 10

    val isEqual = num1 == num2
    println("多Son iguales? $isEqual")

    val isNotEqual = num1 != num2
    println("多Son diferentes? $isNotEqual")

    val isGreater = num1 > num2
    println("多Es mayor? $isGreater")

    val isLess = num1 < num2
    println("多Es menor? $isLess")

}